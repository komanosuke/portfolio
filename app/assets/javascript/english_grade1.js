function nextQuiz() {
    if (quizIndex < quiz.length-1) {
        quizIndex++;
        setUpQuiz();
    } else {
        quizIndex = 0;
        //quiz_yetのpassedIndexを削除
        passedIndex = passedIndex.reverse();
        for(let i = 0; i < passedIndex.length; i++){
            quiz.splice(passedIndex[i], 1);
        }
        passedIndex = [];
        mistakes = [];
        mistakes_answer = [];
        for(let i = 0; i < mistake_list.length; i++){
            mistake_list[i].textContent = '';
        }
        if(quiz.length == 0){
            quiz_list = JSON.parse(JSON.stringify(lists));
            quiz = quiz_list[0];
        }
        setUpQuiz();
    }
}

